  // **********************************************
  // Sort an array of strings 
 public static void sortStrings(String[] a, int SIZE)
 {
     for(int pass=0; pass<= SIZE-2; pass++)
     {
       for(int i=0; i<= SIZE-pass-2; i++)
       {
           if (a[i].compareTo(a[i+1])>0)
               swap(a, i, i+1);
       }
     }
 }

